
SUBROUTINE zqk61_setup_grid (cor,a,b);

! This function determines the points that are necessary to evaluate 
! an integral with the function zqk61n.f between the boundaries a and b.
! 
! Calling arguments: 
! cor: Is an array containing 61 real elements. The coordinates will 
!      be saved in this array and given back to the calling program. 
! a:   Lower integral boundary, real scalar
! b:   Upper integral boundary, real scalar

IMPLICIT NONE
REAL   ,INTENT(INOUT) :: cor(61) 
REAL   ,INTENT(IN)    :: A, B 
REAL                  :: CENTR, HLGTH, DABSC
REAL, DIMENSION(31)   :: XGK
INTEGER               :: J

SAVE XGK
DATA XGK (  1) / 0.999484410050490637571325895705811 /
DATA XGK (  2) / 0.996893484074649540271630050918695 /
DATA XGK (  3) / 0.991630996870404594858628366109486 /
DATA XGK (  4) / 0.983668123279747209970032581605663 /
DATA XGK (  5) / 0.973116322501126268374693868423707 /
DATA XGK (  6) / 0.960021864968307512216871025581798 /
DATA XGK (  7) / 0.944374444748559979415831324037439 /
DATA XGK (  8) / 0.926200047429274325879324277080474 /
DATA XGK (  9) / 0.905573307699907798546522558925958 /
DATA XGK ( 10) / 0.882560535792052681543116462530226 /
DATA XGK ( 11) / 0.857205233546061098958658510658944 /
DATA XGK ( 12) / 0.829565762382768397442898119732502 /
DATA XGK ( 13) / 0.799727835821839083013668942322683 /
DATA XGK ( 14) / 0.767777432104826194917977340974503 /
DATA XGK ( 15) / 0.733790062453226804726171131369528 /
DATA XGK ( 16) / 0.697850494793315796932292388026640 /
DATA XGK ( 17) / 0.660061064126626961370053668149271 /
DATA XGK ( 18) / 0.620526182989242861140477556431189 /
DATA XGK ( 19) / 0.579345235826361691756024932172540 /
DATA XGK ( 20) / 0.536624148142019899264169793311073 /
DATA XGK ( 21) / 0.492480467861778574993693061207709 /
DATA XGK ( 22) / 0.447033769538089176780609900322854 /
DATA XGK ( 23) / 0.400401254830394392535476211542661 /
DATA XGK ( 24) / 0.352704725530878113471037207089374 /
DATA XGK ( 25) / 0.304073202273625077372677107199257 /
DATA XGK ( 26) / 0.254636926167889846439805129817805 /
DATA XGK ( 27) / 0.204525116682309891438957671002025 /
DATA XGK ( 28) / 0.153869913608583546963794672743256 /
DATA XGK ( 29) / 0.102806937966737030147096751318001 /
DATA XGK ( 30) / 0.051471842555317695833025213166723 /
DATA XGK ( 31) / 0.000000000000000000000000000000000 /

CENTR = 0.5*(B+A);
HLGTH = 0.5*(B-A);

do J=1,30
    DABSC = HLGTH*XGK(J);
    cor(2*J-1) = CENTR-DABSC;
    cor(2*J) = CENTR+DABSC;
end do
cor(61) = CENTR;

END SUBROUTINE zqk61_setup_grid
